#!/usr/bin/env node
//
// open-batch-pr.mjs
//
// Branch / commit / push / PR logic for a SCOPED SDK generation batch, extracted
// out of generate-prs.yml so the substance is runnable and testable on a laptop.
//
// Given a freshly-generated SDK working tree (--sdk-dir), this:
//   1. creates the deterministic batch branch  oagen/batch-<batch_id>
//   2. stages the scoped diff and short-circuits cleanly when it is empty
//      (FR-2.7) or manifest-only — no commit, no PR
//   3. builds per-entry commits (feat! → feat → fix → chore) from the classify
//      entries, with a `chore(generated): shared regenerated files` catch-all,
//      so reviewers see focused commits (parity with the previous inline step)
//   4. force-pushes the branch to <remote> so a re-dispatch with the same
//      batch_id UPDATES the one branch instead of opening a second (NFR-2.2)
//   5. creates-or-reuses the PR for that branch
//
// Boundary: git branch/commit/push run against <remote> for real — in the local
// playground <remote> is a bare repo, so idempotence is exercised without
// touching GitHub. Only the `gh` PR mutations are gated by --dry-run, which
// prints the exact command it would run. The .changelog-pending fragment and
// .last-synced-sha plumbing stay in the workflow as thin steps around this
// script (they consume the pr_number/pr_url this script emits).

import { execFileSync } from 'node:child_process';
import { appendFileSync, existsSync, readFileSync, writeFileSync } from 'node:fs';
import { tmpdir } from 'node:os';
import { join } from 'node:path';
import { pathToFileURL } from 'node:url';

const CATCH_ALL_MESSAGE = 'chore(generated): shared regenerated files';
// Files that, on their own, do not warrant a PR: the generator manifest and the
// spec-sync marker the workflow copies in before every run (so it is always
// staged). A diff containing only these is a no-op batch (FR-2.7).
const NOOP_ONLY_FILES = new Set(['.oagen-manifest.json', '.last-synced-sha']);

// ── arg parsing ──────────────────────────────────────────────────────────────
export function parseArgs(argv) {
  const args = {
    batchId: '',
    lang: '',
    services: '',
    sdkDir: '',
    remote: 'origin',
    label: 'autogenerated',
    entriesFile: '',
    bodyFile: '',
    gitUserName: '',
    gitUserEmail: '',
    dryRun: false,
  };
  for (let i = 2; i < argv.length; i += 1) {
    const arg = argv[i];
    if (arg === '--batch-id') args.batchId = argv[++i] ?? '';
    else if (arg === '--lang') args.lang = argv[++i] ?? '';
    else if (arg === '--services') args.services = argv[++i] ?? '';
    else if (arg === '--sdk-dir') args.sdkDir = argv[++i] ?? '';
    else if (arg === '--remote') args.remote = argv[++i] ?? 'origin';
    else if (arg === '--label') args.label = argv[++i] ?? '';
    else if (arg === '--entries-file') args.entriesFile = argv[++i] ?? '';
    else if (arg === '--body-file') args.bodyFile = argv[++i] ?? '';
    else if (arg === '--git-user-name') args.gitUserName = argv[++i] ?? '';
    else if (arg === '--git-user-email') args.gitUserEmail = argv[++i] ?? '';
    else if (arg === '--dry-run') args.dryRun = true;
    else throw new Error(`Unknown option: ${arg}`);
  }
  if (!args.batchId || !args.lang || !args.sdkDir) {
    throw new Error(
      'Usage: open-batch-pr.mjs --batch-id <id> --lang <language> --sdk-dir <path> ' +
        '[--services <csv>] [--remote <name>] [--entries-file <json>] [--body-file <md>] ' +
        '[--git-user-name <n>] [--git-user-email <e>] [--dry-run]',
    );
  }
  return args;
}

// ── pure helpers ───────────────────────────────────────────────────────────────
export function batchBranchName(batchId) {
  return `oagen/batch-${batchId}`;
}

export function parseServices(csv) {
  return String(csv ?? '')
    .split(',')
    .map((s) => s.trim())
    .filter(Boolean);
}

export function prTitle(services, batchId) {
  const list = parseServices(services);
  const label = list.length > 0 ? list.join(', ') : 'all services';
  return `feat(generated): ${label} (batch ${batchId})`;
}

// Order entries feat! → feat → fix → chore (matches the previous inline step).
export function orderedEntries(entries) {
  const order = ['feat!', 'feat', 'fix', 'chore'];
  return order.flatMap((prefix) => (entries ?? []).filter((e) => e.prefix === prefix));
}

export function entryHeadline(entry) {
  const type = entry.prefix.replace(/!$/, '');
  const bang = entry.prefix.endsWith('!') ? '!' : '';
  return `${type}(${entry.scope})${bang}: ${entry.summary}`;
}

// Append ` (#NN)` to each conventional-commit headline inside a
// BEGIN_COMMIT_OVERRIDE block so release-please links each entry. Mirrors the
// awk pass in the previous Create-PR step (only touches lines inside the block).
export function rewriteOverrideRefs(body, prNumber) {
  if (!prNumber) return body;
  let inBlock = false;
  return body
    .split('\n')
    .map((line) => {
      if (line === 'BEGIN_COMMIT_OVERRIDE') {
        inBlock = true;
        return line;
      }
      if (line === 'END_COMMIT_OVERRIDE') {
        inBlock = false;
        return line;
      }
      if (inBlock && /^(feat|fix|chore)(\([^)]+\))?!?: /.test(line)) {
        return `${line} (#${prNumber})`;
      }
      return line;
    })
    .join('\n');
}

// ── git / gh runners ─────────────────────────────────────────────────────────
function git(cwd, gitArgs, { allowFail = false } = {}) {
  try {
    return execFileSync('git', ['-C', cwd, ...gitArgs], { encoding: 'utf8' }).trim();
  } catch (err) {
    if (allowFail) return null;
    throw new Error(`git ${gitArgs.join(' ')} failed: ${(err.stderr || err.message || '').toString().trim()}`);
  }
}

// `git diff --cached --quiet` exits 0 when there is nothing staged, non-zero
// when there is. Invert that into a boolean.
function hasStagedChanges(cwd) {
  try {
    execFileSync('git', ['-C', cwd, 'diff', '--cached', '--quiet'], { stdio: 'ignore' });
    return false;
  } catch {
    return true;
  }
}

// Runs `gh` for real, or — under dryRun — prints the command it would run and
// returns the provided stub. Printing to stdout lets the local path be asserted.
function gh(ghArgs, { dryRun, stub = '' }) {
  if (dryRun) {
    process.stdout.write(`DRY-RUN gh ${ghArgs.join(' ')}\n`);
    return stub;
  }
  return execFileSync('gh', ghArgs, { encoding: 'utf8' }).trim();
}

// ── commit assembly ────────────────────────────────────────────────────────────
function buildCommits(cwd, entries) {
  const initialStaged = git(cwd, ['diff', '--cached', '--name-only'])
    .split('\n')
    .map((l) => l.trim())
    .filter(Boolean);
  const initialSet = new Set(initialStaged);

  for (const entry of orderedEntries(entries)) {
    const entryFiles = (entry.file_paths ?? []).filter((p) => initialSet.has(p));
    if (entryFiles.length === 0) continue; // no attributed files → defer to catch-all

    git(cwd, ['restore', '--staged', '.']);
    git(cwd, ['add', '--', ...entryFiles]);
    if (!hasStagedChanges(cwd)) {
      // Files were already committed under an earlier entry; restore full set.
      git(cwd, ['add', '-A']);
      continue;
    }
    git(cwd, ['commit', '-m', entryHeadline(entry)]);
    git(cwd, ['add', '-A']);
  }

  // Catch-all: behavior-change ripple + any unattributed files.
  if (hasStagedChanges(cwd)) {
    git(cwd, ['commit', '-m', CATCH_ALL_MESSAGE]);
  }
}

function defaultBody(services, batchId, lang) {
  const list = parseServices(services);
  const scope = list.length > 0 ? list.join(', ') : 'all services';
  return `## Summary\n\nScoped regeneration of **${scope}** for \`${lang}\` (batch \`${batchId}\`).\n`;
}

// ── main flow ──────────────────────────────────────────────────────────────────
export function run(args) {
  const { sdkDir, batchId, lang, services, remote, dryRun } = args;
  if (!existsSync(sdkDir)) throw new Error(`--sdk-dir does not exist: ${sdkDir}`);

  const branch = batchBranchName(batchId);

  if (args.gitUserName) git(sdkDir, ['config', 'user.name', args.gitUserName]);
  if (args.gitUserEmail) git(sdkDir, ['config', 'user.email', args.gitUserEmail]);

  // Create (or reset) the deterministic batch branch from the current checkout.
  // In CI each dispatch is a fresh checkout of the SDK's default branch, so this
  // bases the batch on that tip; the force-push later makes re-dispatch update
  // the single remote branch rather than fork a new one.
  git(sdkDir, ['checkout', '-B', branch]);
  git(sdkDir, ['add', '-A']);

  if (!hasStagedChanges(sdkDir)) {
    process.stdout.write(`No changes to commit for batch ${batchId} (${lang}); skipping PR.\n`);
    return { branch, skipped: 'empty-diff', prNumber: null, prUrl: null };
  }

  const staged = git(sdkDir, ['diff', '--cached', '--name-only']).split('\n').filter(Boolean);
  const meaningful = staged.filter((f) => !NOOP_ONLY_FILES.has(f));
  if (meaningful.length === 0) {
    process.stdout.write(
      `Only no-op files (${staged.join(', ')}) changed for batch ${batchId} (${lang}); skipping PR.\n`,
    );
    return { branch, skipped: 'manifest-only', prNumber: null, prUrl: null };
  }

  const entries = args.entriesFile && existsSync(args.entriesFile)
    ? JSON.parse(readFileSync(args.entriesFile, 'utf8'))
    : [];
  buildCommits(sdkDir, entries);

  // Force-push: same batch_id → same branch, overwritten (idempotent, NFR-2.2).
  git(sdkDir, ['push', '--force', remote, branch]);

  // Create-or-reuse the PR for this branch.
  const title = prTitle(services, batchId);
  const bodyText = args.bodyFile && existsSync(args.bodyFile)
    ? readFileSync(args.bodyFile, 'utf8')
    : defaultBody(services, batchId, lang);

  const existing = gh(
    ['pr', 'list', '--head', branch, '--state', 'open', '--json', 'number,url', '--jq', '.[0] | select(.number) | "\\(.number)\\t\\(.url)"'],
    { dryRun, stub: '' },
  );

  let prNumber = null;
  let prUrl = null;
  if (existing) {
    [prNumber, prUrl] = existing.split('\t');
  }

  if (prNumber) {
    // Reuse: refresh title + body on the open PR.
    const body = rewriteOverrideRefs(bodyText, prNumber);
    const bodyPath = writeTempBody(branch, body);
    gh(['pr', 'edit', String(prNumber), '--title', title, '--body-file', bodyPath], { dryRun, stub: '' });
    process.stdout.write(`Reused PR #${prNumber} (${prUrl}) for ${branch}\n`);
  } else {
    const bodyPath = writeTempBody(branch, bodyText);
    const created = gh(
      ['pr', 'create', '--title', title, '--label', args.label, '--head', branch, '--body-file', bodyPath],
      { dryRun, stub: `https://github.com/dry-run/pull/0` },
    );
    prUrl = created;
    prNumber = created ? created.split('/').pop() : null;
    // On a real create, append (#NN) refs into the override block, then update.
    if (!dryRun && prNumber) {
      const body = rewriteOverrideRefs(bodyText, prNumber);
      if (body !== bodyText) {
        const refreshedPath = writeTempBody(branch, body);
        gh(['pr', 'edit', String(prNumber), '--body-file', refreshedPath], { dryRun, stub: '' });
      }
    }
    process.stdout.write(`Created PR ${prUrl} for ${branch}\n`);
  }

  // Under --dry-run no real PR exists; emit unambiguous empty outputs.
  if (dryRun) return { branch, skipped: null, prNumber: null, prUrl: null };
  return { branch, skipped: null, prNumber, prUrl };
}

// PR body is transient and lives outside the SDK tree so it can never be picked
// up by `git add -A`.
function writeTempBody(branch, body) {
  const path = join(tmpdir(), `oagen-batch-pr-body-${branch.replace(/[^a-zA-Z0-9]/g, '-')}.md`);
  writeFileSync(path, body, 'utf8');
  return path;
}

function emitOutputs(result) {
  if (!process.env.GITHUB_OUTPUT) return;
  const lines = [
    `branch=${result.branch}`,
    `skipped=${result.skipped ?? ''}`,
    `pr_number=${result.prNumber ?? ''}`,
    `pr_url=${result.prUrl ?? ''}`,
  ];
  appendFileSync(process.env.GITHUB_OUTPUT, `${lines.join('\n')}\n`);
}

function main() {
  const args = parseArgs(process.argv);
  const result = run(args);
  emitOutputs(result);
}

if (import.meta.url === pathToFileURL(process.argv[1] ?? '').href) {
  try {
    main();
  } catch (err) {
    process.stderr.write(`${err.message}\n`);
    process.exit(1);
  }
}
