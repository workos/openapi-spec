#!/usr/bin/env node
/**
 * Generate the post-mount service roster + per-service endpoint map for
 * `@workos/openapi-spec/services`.
 *
 * `oagen resolve` applies this repo's resolution policy (mountRules, hints) to
 * the spec and emits, per operation, the service it mounts on. Two exports are
 * derived from that:
 *   - `services`: the unique post-mount targets (`mountOn` when an operation is
 *     remounted, otherwise its source `service`) — the services the SDKs expose.
 *   - `serviceEndpoints`: each post-mount service → the operations under it
 *     (`{ method, path, name }`), so tooling can list a service's endpoints.
 *
 * Output: src/policy/services.generated.ts (bundled into dist/services.{mjs,d.mts}
 * by tsdown, so consumers get `import { services, serviceEndpoints } from
 * "@workos/openapi-spec/services"`).
 *
 * Re-run via `npm run generate:services`; `build:policy` runs it automatically.
 */
import { execFileSync } from 'node:child_process';
import { writeFileSync } from 'node:fs';

const SPEC = 'spec/open-api-spec.yaml';
const OUT = 'src/policy/services.generated.ts';

const raw = execFileSync('npx', ['oagen', 'resolve', '--spec', SPEC, '--format', 'json'], {
  encoding: 'utf8',
  maxBuffer: 64 * 1024 * 1024,
  stdio: ['ignore', 'pipe', 'inherit'],
});

/** @type {Array<{ service: string; mountOn?: string | null; method?: string; path?: string; derivedName?: string }>} */
const operations = JSON.parse(raw);
if (!Array.isArray(operations) || operations.length === 0) {
  throw new Error('oagen resolve returned no operations; cannot derive services');
}

// Post-mount service each operation lands on.
const postMount = (op) => op.mountOn || op.service;

const services = [...new Set(operations.map(postMount))]
  .filter(Boolean)
  .sort((a, b) => a.localeCompare(b));

/** @type {Record<string, Array<{ method: string; path: string; name?: string }>>} */
const byService = {};
for (const op of operations) {
  const svc = postMount(op);
  if (!svc || !op.path || !op.method) continue;
  const entry = { method: String(op.method).toUpperCase(), path: op.path };
  if (op.derivedName) entry.name = op.derivedName;
  (byService[svc] ||= []).push(entry);
}
// Alphabetical keys (matching `services`) + endpoints sorted by path then
// method, so the generated output is deterministic build-to-build.
const serviceEndpoints = {};
for (const svc of Object.keys(byService).sort((a, b) => a.localeCompare(b))) {
  serviceEndpoints[svc] = byService[svc].sort(
    (a, b) => a.path.localeCompare(b.path) || a.method.localeCompare(b.method),
  );
}

const body = `// AUTO-GENERATED by scripts/generate-services.mjs — do not edit by hand.
// Run \`npm run generate:services\` (or \`npm run build:policy\`) to refresh.
//
// Derived from the spec via this repo's resolution policy:
//   services         — the post-mount services the generated WorkOS SDKs expose
//   serviceEndpoints — each service's operations ({ method, path, name })

export const services = [
${services.map((s) => `  ${JSON.stringify(s)},`).join('\n')}
] as const;

export type ServiceName = (typeof services)[number];

export interface ServiceEndpoint {
  method: string;
  path: string;
  name?: string;
}

export const serviceEndpoints: Record<string, ServiceEndpoint[]> = ${JSON.stringify(serviceEndpoints, null, 2)};
`;

writeFileSync(OUT, body);
const endpointCount = Object.values(serviceEndpoints).reduce((n, list) => n + list.length, 0);
console.log(`Wrote ${services.length} services and ${endpointCount} endpoints to ${OUT}`);
